-module(graph_db_app).

-behaviour(application).

%% Application callbacks
-export([start/2
        ,stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    Ret = graph_db_sup:start_link(),
    db_manager:load_clients(),
    Ret.

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

